# QuickQuiz: Write a program to print 1 to 50 using a while loop.

i = 1
while(i<51):
    print(i)
    i += 1

# Write a program to print the content of a list using while loops..

l = [1, "Muhammad Ali", False, "This", "Abdul Hakeem", "Moosa Raza", "Messi"]

i = 0

while(i<len(l)):
    print(l[i])
    i += 1
